// Main function to classify, generate reports and show results
function classifyReportAndShow(collection, year) {
    print("Landsat collection size " + year, collection.size());
    // Median image & clip to athens
    var yearlyImage = collection.median().clip(athens);

    // Calculate NDVI, NDBI and MNDWI indices and add them as additional bands 
    var getIndices = function (image) {
        var ndvi = image.normalizedDifference(["B5", "B4"]).rename("NDVI");
        var ndbi = image.normalizedDifference(["B6", "B5"]).rename("NDBI");
        var mndwi = image.normalizedDifference(["B3", "B6"]).rename("MNDWI");
        return image.addBands(ndvi).addBands(ndbi).addBands(mndwi);
    };
    var yearlyImage_plusIndices = getIndices(yearlyImage);

    // Select specific bands for classification
    var classificationBands = ["B2", "B3", "B4", "B5", "B6", "B7", "NDVI", "NDBI", "MNDWI"];
    var yearlyImage_final = yearlyImage_plusIndices.select(classificationBands);
    print("Image before classification " + year, yearlyImage_final);

    // Select training data
    var trainingData = yearlyImage_final.select(classificationBands).sampleRegions({
        collection: trainingSamples,
        properties: ["LandCover"],
        scale: 30,
    });

    // Train a classifier
    var classifier = ee.Classifier.smileRandomForest(10).train({
        features: trainingData,
        classProperty: "LandCover",
        inputProperties: classificationBands
    });

    // Get the confusion matrix and calculate accuracy and kappa of classifier
    var trainAccuracy = classifier.confusionMatrix();
    print("Training overall accuracy " + year, trainAccuracy.accuracy());
    print("Training kappa " + year, trainAccuracy.kappa());

    // Aply classifier to image
    var classifiedImage = yearlyImage_final.classify(classifier);
    print("Classified image " + year, classifiedImage);

    // Create single class intensity map for builtup
    var fromListBuiltUp = [1, 2, 3, 4, 5, 6];
    var toListBuiltUp = [0, 0, 0, 1, 0, 0];
    var onlybuiltUp = classifiedImage.remap({
        from: fromListBuiltUp,
        to: toListBuiltUp,
        defaultValue: 0,
        bandName: 'classification'
    });

    var builtUpNeighbourPerc = onlybuiltUp.reduceNeighborhood({
        reducer: ee.Reducer.mean(),
        kernel: ee.Kernel.circle({
            radius: 1000,
            units: 'meters',
            normalize: false
        })
    });

    var builtUpMask = builtUpNeighbourPerc.updateMask(
        builtUpNeighbourPerc.gte(0.2)
    );

    // Create single class intensity map for vegetation/trees/shrubs
    var fromListGreen = [1, 2, 3, 4, 5, 6];
    var toListGreen = [0, 0, 1, 0, 1, 0];
    var onlyGreen = classifiedImage.remap({
        from: fromListGreen,
        to: toListGreen,
        defaultValue: 0,
        bandName: "classification"
    });

    var greenNeighbourPerc = onlyGreen.reduceNeighborhood({
        reducer: ee.Reducer.sum(),
        kernel: ee.Kernel.circle({
            radius: 300,
            units: "meters",
            normalize: false
        })
    });

    var greenMask = greenNeighbourPerc.updateMask(
        greenNeighbourPerc.gte(11)
    );

    classifiedImage = classifiedImage.mask(builtUpMask);

    Map.addLayer(classifiedImage, landCoverageVisualization, 'Athens_LandCover for  year: ' + year);
    Map.addLayer(builtUpNeighbourPerc, grayIntensityVisualization, 'Athens_BuiltUp Neighbour percentage for  year: ' + year);
    Map.addLayer(builtUpMask, grayIntensityVisualization, 'Athens_BuiltUp Mask for  year: ' + year);
    Map.addLayer(greenNeighbourPerc, greenIntensityVisualization, 'Athens_Green Neighbour percentage for  year: ' + year);
    Map.addLayer(greenMask, greenIntensityVisualization, 'Athens_Green Mask for  year: ' + year);

    Export.image.toDrive({
        image: classifiedImage.visualize(landCoverageVisualization),
        description: 'Athens_LandCover_' + year,
        scale: 50,
        region: athens,
        crs: "EPSG:4326",
        folder: "gee_output"
    });

    createLandCoverPieChart(classifiedImage, year);
    findPopulationNearUGS(greenMask, year);
}

// Function to generate pie charts for land coverage percentages
function createLandCoverPieChart(classifiedImage, year) {
    // First, we want to count the number of pixels in the entire layer for future reference.
    var allpix = classifiedImage.updateMask(classifiedImage); // mask the entire layer
    var pixstats = allpix.reduceRegion({
        reducer: ee.Reducer.count(), // count all pixels in a single class
        geometry: athens,
        scale: 30,
        maxPixels: 1e10
    });

    var allpixels = ee.Number(pixstats.get('classification')); // extract pixel count as a number

    // Then, we want to create an empty list to store the area values we will calculate in
    var arealist = [];

    // Now, we can create a function to derive the extent of one NDVI class
    // The arguments are class number (cnr) and class name (name)
    var areacount = function (cnr, name) {
        var singleMask = classifiedImage.updateMask(classifiedImage.eq(cnr));  // mask a single class
        var stats = singleMask.reduceRegion({
            reducer: ee.Reducer.count(), // count pixels in a single class
            geometry: athens,
            scale: 30,
            maxPixels: 1e10
        });
        var pix = ee.Number(stats.get('classification'));
        var hect = pix.multiply(900).divide(10000); // Landsat pixel = 30m x 30m --> 900 sqm
        var perc = pix.divide(allpixels).multiply(10000).round().divide(100);   // get area percent by class and round to 2 decimals
        arealist.push({ Class: name, Pixels: pix, Hectares: hect, Percentage: perc });
    };

    var classNames = ['bare soil', 'crop', 'grass', 'builtup', 'trees', 'water'];

    // execute function for each class
    for (var i = 0; i < classNames.length; i++) {
        areacount(i + 1, classNames[i]);
    }

    // Create a feature for a landcover class that includes the area covered.
    function createFeature(landcover_class_stats) {
        landcover_class_stats = ee.Dictionary(landcover_class_stats);
        var result = {
            landcover_class_name: landcover_class_stats.get('Class'),
            area_percentage: landcover_class_stats.get('Percentage')
        };
        return ee.Feature(null, result); // Creates a feature without a geometry.
    }

    var landcover_fc = ee.FeatureCollection(arealist.map(createFeature));

    // Add a summary chart.
    var landcover_summary_chart = ui.Chart.feature.byFeature({
        features: landcover_fc,
        xProperty: 'landcover_class_name',
        yProperties: ['area_percentage']
    })
        .setChartType('PieChart')
        .setOptions({
            title: 'Summary of landcover class areas ' + year,
            colors: landCoverageVisualization.palette,
            sliceVisibilityThreshold: 0 // Don't group small slices.
        });
    print(landcover_summary_chart);
}

// Function used to find population which has access to urban green spaces
function findPopulationNearUGS(greenMask, year) {
    var populationAthensCount = ee.Number(populationAthens.reduceRegion({
        reducer: ee.Reducer.sum(),
        geometry: athens,
        scale: 100,
    }).get('population'));
    var populationAthensNearUGS = populationAthens.mask(greenMask);
    var populationAthensNearUGSCount = ee.Number(populationAthensNearUGS.reduceRegion({
        reducer: ee.Reducer.sum(),
        geometry: greenMask.geometry(),
        scale: 100,
    }).get('population'));
    var ai = (populationAthensNearUGSCount.divide(populationAthensCount)).multiply(100).format('%.2f')
        .cat("% of urban green spaces");
    print(ai);
    Export.image.toDrive({
        image: populationAthensNearUGS.visualize(greenIntensityVisualization),
        description: 'Athens_Population_Near_UGS_' + year,
        scale: 50,
        region: athens,
        crs: "EPSG:4326",
        folder: "gee_output"
    });
    Map.addLayer(populationAthensNearUGS, greenIntensityVisualization, 'Athens_Population near UGS for  year: ' + year);
}

